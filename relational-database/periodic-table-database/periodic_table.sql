-- Periodic Table Database
-- This script creates a database of chemical elements and their properties

-- Create the database
-- Run: createdb periodic_table
-- Then connect: psql -d periodic_table -f periodic_table.sql

-- Create tables
CREATE TABLE properties (
    atomic_number INTEGER PRIMARY KEY,
    type VARCHAR(20) NOT NULL,
    atomic_mass DECIMAL(9,6) NOT NULL,
    melting_point_celsius DECIMAL(8,2),
    boiling_point_celsius DECIMAL(8,2),
    type_id INTEGER
);

CREATE TABLE elements (
    atomic_number INTEGER PRIMARY KEY,
    symbol VARCHAR(2) UNIQUE NOT NULL,
    name VARCHAR(40) UNIQUE NOT NULL
);

CREATE TABLE types (
    type_id SERIAL PRIMARY KEY,
    type VARCHAR(20) UNIQUE NOT NULL
);

-- Add foreign key constraints
ALTER TABLE properties ADD CONSTRAINT fk_properties_elements
    FOREIGN KEY (atomic_number) REFERENCES elements(atomic_number);

ALTER TABLE properties ADD CONSTRAINT fk_properties_types
    FOREIGN KEY (type_id) REFERENCES types(type_id);

